def addition(a,b):
    return f"The sum of the numbers is: {a+b}"

def subtraction(a,b):
    return f"The difference of the two numbers is: {abs((a-b))}"

def multiplication(a,b):
    return f"The product of the numbers {a , b} is: {a*b}"

def division(a,b):
    return f"The quotient when {a} divided by {b} is: {a/b}"